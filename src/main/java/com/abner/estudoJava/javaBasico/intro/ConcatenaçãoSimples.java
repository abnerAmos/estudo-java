package com.abner.estudoJava.javaBasico.intro;

public class ConcatenaçãoSimples {

    public static void main(String[] args) {


        String nome, segundoNome, ultimoNome;
        nome = "Abner ";
        segundoNome = "Amos ";
        ultimoNome = "de Souza";
        int idade = 29;

        System.out.println(nome+segundoNome+ultimoNome);
        System.out.println(idade);
    }
}
